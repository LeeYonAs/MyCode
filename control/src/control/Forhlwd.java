package control;

public class Forhlwd {

	public static void main(String[] args) {
		for(int i=0;i<5;i++) {
			System.out.println("the "+i+" times say Hello Java");
		}
		System.out.println("--------------------------");
		
		for(int x=1; x<=3; x++) {
			System.out.println("HelloWorld");
		}	
		System.out.println("--------------------------");
		
		for(int x=1; x<3; x++) {
			System.out.println("HelloWorld");
		}
		System.out.println("--------------------------");
		
		for(int x=3; x>0; x--) {
			System.out.println("HelloWorld");
		}
		System.out.println("--------------------------");
	}

}
